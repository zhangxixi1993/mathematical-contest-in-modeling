close all
syms I T P                  %设置变量

F=0.2918*(I-5.4238-2.4769-0.1215*(T+273.15+(I*(0.0579*I+0.15*log(1+1.03e5*I))-P)*1.8048)+8.1135e-4*(T+273.15+(I*(0.0579*I+0.15*log(1+1.03e5*I))-P)*1.8048)^2-1.6823e-7*(T+273.15+(I*(0.0579*I+0.15*log(1+1.03e5*I))-P)*1.8048)^3-3.0193e-9*(T+273.15+(I*(0.0579*I+0.15*log(1+1.03e5*I))-P)*1.8048)^4)-P;%10摄氏度
